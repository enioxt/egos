const REPOS = [
  { name: 'egos', path: '/home/enio/egos', priority: 'P0' },
  { name: 'egos-lab', path: '/home/enio/egos-lab', priority: 'P1' },
  { name: '852', path: '/home/enio/852', priority: 'P1' },
  { name: 'forja', path: '/home/enio/forja', priority: 'P1' },
  { name: 'carteira-livre', path: '/home/enio/carteira-livre', priority: 'P1' },
  { name: 'commons', path: '/home/enio/commons', priority: 'P2' },
  { name: 'santiago', path: '/home/enio/santiago', priority: 'P2' },
  { name: 'br-acc', path: '/home/enio/br-acc', priority: 'P2' },
  { name: 'smartbuscas', path: '/home/enio/smartbuscas', priority: 'P2' },
];

const SECTIONS = {
  inbox: join(VAULT_PATH, '00 - Inbox'),
  raw: join(VAULT_PATH, '01 - Raw Sources'),
  wiki: join(VAULT_PATH, '02 - Wiki'),
  sessions: join(VAULT_PATH, '03 - Sessions'),
  outputs: join(VAULT_PATH, '04 - Outputs'),
  templates: join(VAULT_PATH, '05 - Templates'),
  archive: join(VAULT_PATH, '99 - Archive'),
};

interface ExportStats {
  filesCopied: number;
  filesGenerated: number;
  errors: string[];
  reposProcessed: number;
}

async function ensureStructure(): Promise<void> {
  for (const [key, path] of Object.entries(SECTIONS)) {
    if (!existsSync(path)) {
      await mkdir(path, { recursive: true });
      console.log(`📁 Created: ${path}`);
    }
  }
}

async function copyRawSources(): Promise<number> {
  let count = 0;
  
  for (const repo of REPOS) {
    if (!existsSync(repo.path)) {
      console.log(`⚠️ Skip: ${repo.name} (not found)`);
      continue;
    }

    const repoRawPath = join(SECTIONS.raw, repo.name);
    await mkdir(repoRawPath, { recursive: true });

    // Priority files to copy
    const priorityFiles = [
      'AGENTS.md',
      'TASKS.md',
      'README.md',
      'HARVEST.md',
      'docs/',
      '.guarani/',
    ];

    for (const file of priorityFiles) {
      const srcPath = join(repo.path, file);
      if (existsSync(srcPath)) {
        const destPath = join(repoRawPath, file);
        try {
          if ((await Bun.file(srcPath).stat()).isDirectory()) {
            // Copy directory recursively
            await $`cp -r ${srcPath} ${destPath}`.quiet();
          } else {
            await copyFile(srcPath, destPath);
          }
          count++;
        } catch (e) {
          console.log(`⚠️ Failed to copy ${file} from ${repo.name}: ${e}`);
        }
      }
    }

    console.log(`✅ ${repo.name}: ${count} files`);
  }

  return count;
}

