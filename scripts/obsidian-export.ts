async function createObsidianConfig(): Promise<void> {
  const configDir = join(VAULT_PATH, '.obsidian');
  await mkdir(configDir, { recursive: true });

  // App.json
  const appConfig = {
    alwaysUpdateLinks: true,
    newFileLocation: 'folder',
    newFileFolderPath: '00 - Inbox',
    attachmentFolderPath: '99 - Archive/attachments',
    promptDelete: false,
  };

  await writeFile(
    join(configDir, 'app.json'),
    JSON.stringify(appConfig, null, 2)
  );

  // Hotkeys for common actions
  const hotkeys = {
    'obsidian-metdata-menu:open-metadata-menu': [
      { modifiers: ['Mod'], key: 'm' }
    ],
  };

  await writeFile(
    join(configDir, 'hotkeys.json'),
    JSON.stringify(hotkeys, null, 2)
  );

  console.log(`⚙️ Obsidian config: ${configDir}`);
}

export async function main(): Promise<void> {
  console.log('🚀 EGOS → Obsidian Export\n');
  
  const stats: ExportStats = {
    filesCopied: 0,
    filesGenerated: 0,
    errors: [],
    reposProcessed: 0,
  };

  try {
    // 1. Ensure structure
    console.log('📁 Ensuring vault structure...');
    await ensureStructure();

    // 2. Copy raw sources
    console.log('\n📥 Copying raw sources...');
    stats.filesCopied = await copyRawSources();
    stats.reposProcessed = REPOS.filter(r => existsSync(r.path)).length;

    // 3. Generate wiki
    console.log('\n📝 Generating wiki...');
    await generateWikiIndex();
    stats.filesGenerated++;

    // 4. Generate dashboard
    console.log('\n📊 Generating dashboard...');
    await generateDashboard();
    stats.filesGenerated++;

    // 5. Generate session entry
    console.log('\n📄 Generating session entry...');
    await generateSessionEntry();
    stats.filesGenerated++;

    // 6. Obsidian config
    console.log('\n⚙️ Creating Obsidian config...');
    await createObsidianConfig();

    // Summary
    console.log('\n' + '='.repeat(50));
    console.log('✅ Export complete!\n');
    console.log(`Repos processed: ${stats.reposProcessed}`);
    console.log(`Files copied: ${stats.filesCopied}`);
    console.log(`Files generated: ${stats.filesGenerated}`);
    console.log(`\nVault: ${VAULT_PATH}`);
    console.log('\nNext: Open Obsidian → Open folder as vault → Select:');
    console.log(`  ${VAULT_PATH}`);
    console.log('='.repeat(50));

  } catch (error) {
    console.error('❌ Export failed:', error);
    process.exit(1);
  }
}

// Run if called directly
if (import.meta.main) {
  main();
}
